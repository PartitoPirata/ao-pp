			<br />
			Al termine della via vi troverete di fronte alla Torre di Porta al Prato, quindi svoltare a sinistra su Viale Fratelli Rosselli e avanti circa 2 km fino a Piazza della Liberta', procedete avanti (seguendo le indicazioni Stadio) su Viale Don Giovanni Minzoni.
			<br />
			Al termine del viale svoltate a destra e poi attraversate il cavalcavia sulla ferrovia, quindi svoltate subito a destra e procedete per circa 1 km sul Viale dei Mille. Svoltate a destra in Via Settesanti e alla fine della strada svoltate ancora a destra su Via Campo D'Arrigo procedendo per altri 230 metri.
